#include <bits/stdc++.h>
using namespace std;
class Solution {
  public:
    int setBits(int n) {
        int count=0;
        while(n>0)
        {
            if(n%2==1)
            {
                count++;
                n=n/2;
            }
            else n=n/2;
        }
    return count;
    }
};